class Switch{
public static void main(String args[])
{
int n=10;
switch(n){
case 4:
System.out.println("number is 4");
break;
case 7:
System.out.println("number is 7");
break;
case 10:
System.out.println("number is 10");
break;
case 0:
System.out.println("number is 0");
break;

default:
System.out.println("number is not");
}
}
} 