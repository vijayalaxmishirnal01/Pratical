class Ifelseif{
public static void main(String args[])
{
String City="Solapur";
if(City== "Pune"){
System.out.println("City is Pune");
}
else if(City== "Delhi"){
System.out.println("City is Delhi");
}
else if(City== "Mumbai"){
System.out.println("City is Mumbai");
}
else{
System.out.println("City is Solapur");
}
}
}